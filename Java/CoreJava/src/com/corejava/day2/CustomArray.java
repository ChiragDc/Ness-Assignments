package com.corejava.day2;

public class CustomArray {
    public static void main(String[] args) {
        int arr[]= {10, 12, 20, 30, 25, 40, 32, 31, 35, 50, 60};
        System.out.println("index between 3 and 8:");
        for (int i=3;i<7;i++){
            System.out.println(arr[i]);
        }
    }
}
